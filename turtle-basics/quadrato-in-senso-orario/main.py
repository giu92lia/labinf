# Il programma disegna un quadrato, in senso antiorario.

import turtle

pippo = turtle.Turtle(); # crea una tartaruga, chiamata pippo

pippo.forward(100);
pippo.left(90);
pippo.forward(100);
pippo.left(90);
pippo.forward(100);
pippo.left(90);
pippo.forward(100);

turtle.done()

# disegna un quadrato in senso orario

pippo.forward(100)
pippo.right(90)
pippo.forward(100)
pippo.right(90)
pippo.forward(100)
pippo.right(90)
pippo.forward(100)
turtle.done()
